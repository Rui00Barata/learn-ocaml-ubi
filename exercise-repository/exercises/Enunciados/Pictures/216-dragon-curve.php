<h1>Dragon Curve</h1>
  <img src="images/0216-i.jpg" alt="dragon curve tiling the plane"  />  
<p>The dragon curve is a fractal curve that you can easily fold out of paper. In the image above, four dragon curves are placed together to show how they tile the plane. John Heighway discovered this curve that is sometimes called the Heighway dragon.</p>
  <br>

<h2>Steps</h2>
  <ol>
    <li>Cut eight strips of paper, two strips of each of the four colors. I made strips 1 inch wide (2.5 cm) by 11 inches long. <br class="u-cf"></li>
    <li><img src="images/0216-s02.png" class="u-pull-right"  />Fold a strip in half by bringing the right edge on top of the left edge. <br class="u-cf"></li>
    <li><img src="images/0216-s03.png" class="u-pull-right"  />Fold the strip in half again right edge on top of left edge. <br class="u-cf"></li>
    <li><img src="images/0216-s03c.jpg" class="u-pull-right"  />Fold the strip in half again two more times for a total of four folds always folding in the same direction. <br class="u-cf"></li>
     <li><img src="images/0216-s03b.jpg" class="u-pull-right"  />Unfold the strip of paper so that each fold is a right angle. This image shows what the strip of paper would look like if you opened it after the first fold, the second fold and so on. The shape in the lower right is what your strip of paper should look like.<br class="u-cf"></li>
         <li><img src="images/0216-s04.jpg" class="u-pull-right"  />Fold another curve and tape the two curves together as shown. Repeat for the other three colors.<br class="u-cf"></li>
	<li><img src="images/0216-s05.jpg" class="u-pull-right"  />Place two curves together.<br class="u-cf"></li>
	<li><img src="images/0216-s06.jpg" class="u-pull-right"  />Add the remaining two curves to the pattern. Each curve starts at the same point but is rotated 90&deg;.<br class="u-cf"></li>
    	<li><img src="images/0216-i.jpg" class="u-pull-right"  />You can extend the curves by cutting and folding two more strips of each color and adding them to the pattern as shown.<br class="u-cf"></li>
  </ol>

<h2>Links</h2>
  <ul>
    <li><a href="http://en.wikipedia.org/wiki/Dragon_curve">Wikipedia's article</a> on dragon curves includes an animated GIF showing the development of the fractal curve after each itteration. After about 10 itterations, you can see the dragon-like shape that gives this curve its name.</li>
  </ul>
